public class Main {

    public static void main(String[] args) {

        // The first homework "Variables"

        byte b1 = 12;                   // 12 in decimal number system
        short s1 = 1300;
        int i1 = 777_777;
        long l1 = 123456789l;
        float f1 = 3.14f;
        float f2 = 5.78F;
        double d1 = 87.89d;
        double d2 = 69.99D;
        boolean c1 = true;
        boolean c2 = false;
        char v1 = 'a';                  // a
        char v2 = 65;                   // A

        System.out.println("byte is: " + b1);
        System.out.println("short is: " + s1);
        System.out.println("int is: " + i1);
        System.out.println("long is: " + l1);
        System.out.println("float first is: " + f1);
        System.out.println("float second is: " + f2);
        System.out.println("double first is " + d1);
        System.out.println("double second is " + d2);
        System.out.println("boolean first: " + c1);
        System.out.println("boolean second: " + c2);
        System.out.println("char first is " + v1);
        System.out.println("char second is " + v2);
    }
}
